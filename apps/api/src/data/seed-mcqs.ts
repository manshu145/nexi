const SME = 'manshu145@nexigrate.com';
const APPROVED_AT = asISODateTime('2026-05-22T00:00:00.000Z');

function mcq(
  id: string,
  exam: string,
  subject: string,
  chapter: string,
  question: string,
  options: { key: 'A' | 'B' | 'C' | 'D'; text: string }[],
  correctOption: 'A' | 'B' | 'C' | 'D',
  explanation: string,
  difficulty: 'easy' | 'medium' | 'hard',
  source: string,
): MCQ {
  return {
    id: asMcqId(id),
    exam: asExamSlug(exam),
    subject: subject as MCQ['subject'],
    chapter: chapter as MCQ['chapter'],
    question,
    options,
    correctOption,
    explanation,
    difficulty,
    source,
    verifiers: [],
    smeApprovedBy: SME,
    smeApprovedAt: APPROVED_AT,
    isPublished: true,
    createdAt: APPROVED_AT,
    updatedAt: APPROVED_AT,
  };
}

